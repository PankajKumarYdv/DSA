import java.lang.*;
class differentDatatype{
    public static void main(String arg[]){
        byte b = 87; 
        short s = 2342; 
        int i = 200; 

        float f = 234.234f; 
        char c = 'a'; 

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(f);
        System.out.println(c);
    }
}